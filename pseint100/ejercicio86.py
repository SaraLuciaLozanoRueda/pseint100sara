dia = int(input("INGRESE DIA [1 - 7]: "))

if dia == 1:
    print("LUNES")
elif dia == 2:
    print("MARTES")
elif dia == 3:
    print("MIÉRCOLES")
elif dia == 4:
    print("JUEVES")
elif dia == 5:
    print("VIERNES")
elif dia == 6:
    print("SÁBADO")
elif dia == 7:
    print("DOMINGO")
else:
    print("DÍA INVALIDO")