letra=input("LETRA [ V- P - C - J - F ]:\n ")
if letra=="V" or letra=="v":
    print("Visual Basic")
elif letra=="P" or letra=="p":
    print("Pascal")
elif letra=="C" or letra=="c":
    print("C#")
elif letra=="J" or letra=="j":
    print("Java")
elif letra=="F" or letra=="f":
    print("fortran")
else:
    print("RECUERDA INGRESAR SOLO LAS LETRAS VALIDAS, son las que aparecen en []")